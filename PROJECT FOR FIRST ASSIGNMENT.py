# Constants are usually written in ALL_CAPS by convention
PI = 3.14159   # Constant value for pi

# Variables (values can change)
radius = 5
height = 20


# Calculations using variables and constants
circle_area = PI * radius ** 2
cylinder_volume = PI * radius ** 2 * height

# Output results using print()
print("Radius:", radius)
print("Height:", height)
print("Circle Area:", circle_area)
print("Cylinder Volume:", cylinder_volume)